package org.technbolts.scoop.exception;

@SuppressWarnings("serial")
public class CombinaisonsException extends ScoopException {

    public CombinaisonsException() {
        super();
    }

    public CombinaisonsException(String message, Throwable cause) {
        super(message, cause);
    }

    public CombinaisonsException(String message) {
        super(message);
    }

    public CombinaisonsException(Throwable cause) {
        super(cause);
    }

}
